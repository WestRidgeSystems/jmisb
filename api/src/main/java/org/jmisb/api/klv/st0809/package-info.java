/**
 * ST 0809: Meteorological Metadata Local Set.
 *
 * <p>This Standard defines KLV metadata to convey meteorological information within a Local Set
 * construct.
 *
 * <p>Advanced Geospatial Intelligence (AGI) (also known as Imagery Derived MASINT) entails the
 * quantitative analysis of imagery sensor data. Fundamental to almost all AGI techniques is the
 * characterization of the atmosphere between an observed object/activity and the sensor that
 * records it. This standard provides both the language with which to describe the atmosphere and
 * the mechanisms to convey that knowledge.
 *
 * <p>There is no intent in this document to imply the exploitation of any particular phenomenology
 * in any specific region(s) of the electromagnetic spectrum. Rather, this document establishes the
 * means to describe and convey a broad range of basic information about the atmosphere applicable
 * to many purposes.
 */
